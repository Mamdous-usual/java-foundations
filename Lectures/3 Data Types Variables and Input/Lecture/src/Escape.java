public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello 'Mamdous'");
        System.out.println("Hello \'Mamdous\'");
        System.out.println("Hello \"Mamdous\"");
        System.out.println("Hello \\Mamdous\\");
        System.out.println("Hello\bMamdous");
        System.out.println("Hello\tMamdous");
        System.out.println("Hello\nMamdous");
    }
}
